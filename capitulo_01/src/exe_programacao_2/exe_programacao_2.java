package exe_programacao_2;

public class exe_programacao_2 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		imprimeCabelo();
		imprimeOlhos();
		imprimeNariz();
		imprimeBoca();
		imprimeQueixo();		
	}
	
	/**
	 * Metodo que imprime o cabelo
	 */
	private static void imprimeCabelo() {
		System.out.println("  /////");
	}
	
	/**
	 * Metodo que imprime os olhos
	 */
	private static void imprimeOlhos() {	
		System.out.println(" | o o |");
	}
	
	/**
	 * Metodo que imprime o nariz
	 */
	private static void imprimeNariz() {
		System.out.println("(|  ^  |)");
	}
	
	/**
	 * Metodo que imprime a boca
	 */
	private static void imprimeBoca() {
		System.out.println(" | [_] |");		
	}
	
	/**
	 * Metodo que imprime o queixo
	 */
	private static void imprimeQueixo() {
		System.out.println("  -----");		
	}
}
